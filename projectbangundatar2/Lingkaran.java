public class Lingkaran {
    int hitungLuas(int l1){
        int Luas = l1 * l1;
        return Luas;
    }
    
    double hitungLuas(double l1){
        double Luas = l1 * l1;
        return Luas;
    }
    
    //Kelilingnya
    int hitungKeliling(int l1){
        int Keliling = 2 * l1;
        return Keliling;
    }
    
    double hitungKeliling(double l1){
        double Keliling = 2 * l1;
        return Keliling;
    }
}
