public class Operasi {
    int jumlahkan(int satu, int dua){
        return (satu+dua);
    }
    
    int jumlahkan(int satu, int dua, int tiga){
        return (satu+dua+tiga);
    }
    
    double jumlahkan(double satu, double dua, double tiga){
        return (satu + dua + tiga);
    }
    
    double kalikan(int satu, double dua){
        
        return (satu * dua);
        
    }
}
